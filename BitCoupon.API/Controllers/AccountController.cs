        /// <summary>
        /// Resets users password
        /// </summary>
        /// <param name="model">model (email, password and 
        /// reset token received with email)</param>
        /// <returns></returns>
        [AllowAnonymous]
        [HttpPost]
        [Route("ResetPassword")]
        public IHttpActionResult ResetPassword(ResetPasswordViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = UserManager.FindByEmail(model.Email);

                if (user != null)
                {
                    string token = model.ResetToken.Replace(" ", "+");
                    var result = UserManager.ResetPassword(user.Id, token, model.Password);    
                    return Ok();
                }
                return NotFound();
            }
            return BadRequest(ModelState);
        }

        /// <summary>
        /// Edits user profile 
        /// </summary>
        /// <param name="model">edit profile bind model</param>
        /// <returns></returns>
        [HttpPost]
        [Route("EditProfile")]
        public IHttpActionResult EditProfile(EditProfileBindingModel model)
        {
            if (ModelState.IsValid)
            {
                var userEmail = this.User.Identity.Name;
                var user = UserManager.FindByEmail(userEmail);

                if (user != null)
                {
                    user.Email = model.Email;
                    user.AvatarUrl = model.AvatarUrl;
                    user.Address = model.Address;
                    user.Location = model.Location;
                    user.PhoneNumber = model.PhoneNumber;

                    var result = UserManager.Update(user);
                    if (result.Succeeded)
                    {
                        var store = new UserStore<ApplicationUser>(db);
                        var ctx = store.Context;
                        ctx.SaveChanges();
                        return Ok();
                    }
                    return BadRequest(ModelState);
                }
            }
            return BadRequest(ModelState);
        }

